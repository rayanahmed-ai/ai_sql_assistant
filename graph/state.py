    # USER INPUT
    # =====================================

    question: str

    # =====================================
    # NLP OUTPUT
    # =====================================

    cleaned_query: str

    # =====================================
    # DATABASE SCHEMA
    # =====================================

    schema: str

    # =====================================
    # RAG CONTEXT
    # =====================================

    rag_context: str

    # =====================================
    # GENERATED SQL
    # =====================================

    generated_sql: str

    # =====================================
    # VALIDATION
    # =====================================

    is_valid: bool

    validation_reason: str

    # =====================================
    # QUERY RESULT
    # =====================================

    query_result: str

    # =====================================
    # EXPORT
    # =====================================

    export_path: str